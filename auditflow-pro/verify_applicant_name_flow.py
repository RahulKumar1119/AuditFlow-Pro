def test_reporter_applicant_name_extraction():
    """Test that reporter correctly extracts applicant name from golden record."""
    print("\n" + "="*80)
    print("TEST 3: Reporter Applicant Name Extraction")
    print("="*80)
    
    # Simulate golden record
    golden_record = {
        'name': {
            'value': 'John Doe',
            'source_document': 'doc-test-001',
            'confidence': 0.95
        }
    }
    
    # Simulate reporter logic
    if 'name' in golden_record and isinstance(golden_record['name'], dict):
        applicant_name = golden_record['name'].get('value', '').strip()
    else:
        applicant_name = "Unknown Applicant"
    
    print("\n✓ Applicant name extracted from golden record")
    print(f"  - Applicant Name: {applicant_name}")
    
    # Verify extraction
    assert applicant_name == 'John Doe', f"Expected 'John Doe', got '{applicant_name}'"
    assert applicant_name != 'Unknown Applicant', "Applicant name should not be 'Unknown Applicant'"
    
    print("\n✓ Applicant name extraction is correct")
    
    return applicant_name


